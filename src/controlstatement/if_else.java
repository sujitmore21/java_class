package controlstatement;

public class if_else {

    public static void main(String[] args) {
        int n = 5;

        if (n==0){
            System.out.println("nothing");
            System.out.println("bye");
        }

        else if (n==0) {
            System.out.println("Even");
        }
        else {
            System.out.println("odd");

        }
    }
}
