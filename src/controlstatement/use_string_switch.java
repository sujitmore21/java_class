package controlstatement;

public class use_string_switch {
    public static void main(String[] args) {
        String d = "abc";

        switch (d) {
            case "abc":
                System.out.println("one");
                break;
            case "pqr":
                System.out.println("one");
                break;
            default:
                System.out.println("No match");

        }
    }
}
